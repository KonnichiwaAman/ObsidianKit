export function getToolsByCategory(categoryId: string): Tool[] {
  return toolsByCategory.get(categoryId) ?? EMPTY_TOOLS;
}

export function getToolById(id: string): Tool | undefined {
  return toolsById.get(id);
}

export function getPopularTools(): Tool[] {
  return popularTools;
}

const EMPTY_TOOLS: Tool[] = Object.freeze([]) as unknown as Tool[];

const toolsById = new Map<string, Tool>();
const groupedTools = new Map<string, Tool[]>();
const popularToolsList: Tool[] = [];

for (const tool of tools) {
  toolsById.set(tool.id, tool);

  const byCategory = groupedTools.get(tool.categoryId);
  if (byCategory) {
    byCategory.push(tool);
  } else {
    groupedTools.set(tool.categoryId, [tool]);
  }

  if (tool.isPopular) {
    popularToolsList.push(tool);
  }
}

const toolsByCategory = new Map<string, Tool[]>(
  Array.from(groupedTools.entries(), ([categoryId, categoryTools]) => [
    categoryId,
    Object.freeze([...categoryTools]) as unknown as Tool[],
  ]),
);

const popularTools: Tool[] = Object.freeze([...popularToolsList]) as unknown as Tool[];
